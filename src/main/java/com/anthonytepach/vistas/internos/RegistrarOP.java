    private void carbarCombo() {
        C_Producto cpro = new C_Producto();
        ResultSet consulta;
        try {
            consulta = cpro.getProducts();
            while (consulta.next()) {
                jcb_productos.addItem(consulta.getString(2));
                arrayCod.add(consulta.getString(1));
            }
        } catch (Exception ex) {
            loggger.setError(ex.getMessage(), ex.fillInStackTrace());
        }
    }


