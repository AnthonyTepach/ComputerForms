    private void guardarOP() {
        try {
            C_OrdenProduccion c_op = new C_OrdenProduccion();
            c_op.insertOP(getData(), rootPane);
        } catch (Exception ex) {
            loggger.setError(ex.getMessage(), ex.fillInStackTrace());
        }
    }

    private OrdenProduccion getData() {
        OrdenProduccion op = new OrdenProduccion();
        String type = null;
        op.setNo_op(jtf_op.getText());
        if (jrb_normal.isSelected()) {
            type = "Normal";
        } else if (jrb_rush.isSelected()) {
            type = "Rush";
        } else if (jrb_reproceso.isSelected()) {
            type = "Reproceso";
        }
        op.setTipo(type);
        op.setFecha_ini(jdch_fini.getDate());
        op.setFecha_fin(jdch_ffin.getDate());
        op.setStatus("Produccion");
        op.setProducto(arrayCod.get(jcb_productos.getSelectedIndex()));
        op.setClave_proyecto(clave_proyecto);
        op.setNo_cotizacion(jtf_cotizacion.getText());
        op.setDesc(jta_desc_froma.getText());
        op.setNom_forma(jtf_nombreforma.getText());
        return op;
    }

    private void limpiar() {
        jtf_op.setText(getOPini());
        if (jrb_normal.isSelected()) {
           jrb_normal.setSelected(false);
        } else if (jrb_rush.isSelected()) {
           jrb_rush.setSelected(false);
        } else if (jrb_reproceso.isSelected()) {
           jrb_reproceso.setSelected(false);
        }
        jdch_ffin.setDate(null);
        jdch_fini.setDate(null);
        jcb_productos.setSelectedIndex(-1);
        jtf_nombreforma.setText(null);
        jta_desc_froma.setText(null);
        jtf_cotizacion.setText(null);
    }
