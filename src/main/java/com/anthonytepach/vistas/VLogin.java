    private void jl_exitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jl_exitMouseClicked
        System.gc();
        int a = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de salir?", "Confirmación", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            try {
                Thread.sleep(1000);
                JOptionPane.showMessageDialog(rootPane, "Hasta pronto", "Saliendo....", JOptionPane.INFORMATION_MESSAGE);
            } catch (InterruptedException ex) {
                Logger.getLogger(VLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            System.exit(a);
        }

    }//GEN-LAST:event_jl_exitMouseClicked

