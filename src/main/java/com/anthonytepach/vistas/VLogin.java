    private void login() {
        Usuarios user = new Usuarios();
        user.setUsername(jtf_usuario.getText());
        user.setPassword(jps_password.getText());
        C_Usuario cu = new C_Usuario();
        if (cu.loguear(user)) {
            this.setVisible(false);
            VMenu vm = new VMenu(cu.getUserL());
            vm.setVisible(true);
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(rootPane, "El usuario y/o contraseña son incorrectos", "Datos invalidos", JOptionPane.ERROR_MESSAGE);
        }
    }

